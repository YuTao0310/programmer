package multiplethread;
 
import java.util.LinkedList;
 
public class ThreadPool {
 
    // 线程池大小
    int threadPoolSize;
 
    // 任务容器，相当于Prodecer和Consumer中的Stack
    LinkedList<Runnable> tasks = new LinkedList<Runnable>();
 
    // 试图消费任务的线程
 
    public ThreadPool() {
        threadPoolSize = 10;
 
        // 启动10个任务消费者线程
        /** 相当于ProducerConsumer的main中 */
        /** 这个synchronized(tasks)其实也可以省略 */
        synchronized (tasks) {
            for (int i = 0; i < threadPoolSize; i++) {
                new TaskConsumeThread("任务消费者线程 " + i).start();
            }
        }
    }
 
    /** 相当于Producer */
    public void add(Runnable r) {
        /** 相当于ProducerConsumer中stack的push操作 */
        synchronized (tasks) {
            tasks.add(r);
            // 唤醒等待的任务消费者线程
            tasks.notifyAll();
        }
    }
 
    /** 相当于Consumer */
    class TaskConsumeThread extends Thread {
        public TaskConsumeThread(String name) {
            super(name);
        }
 
        Runnable task;
 
        public void run() {
            System.out.println("启动： " + this.getName());
            while (true) {
                /** 相当于ProducerConsumer中的Stack的pull操作 */
                synchronized (tasks) {
                    while (tasks.isEmpty()) {
                        try {
                            tasks.wait();
                        } catch (InterruptedException e) {
                            e.printStackTrace();
                        }
                    }
                    task = tasks.removeLast();
                    // 允许添加任务的线程可以继续添加任务
                    tasks.notifyAll();
 
                }
                System.out.println(this.getName() + " 获取到任务，并执行");
                task.run();
            }
        }
    }
 
}